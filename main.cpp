// [1, 2, 4, 5, 8]

// О(n^2)

void selectionSort(int arr[], int n) {
    for (int i = 0; i < n - 1; i++) {
        int minIndex = i;

        // Поиск минимального элемента
        for (int j = i + 1; j < n; j++) {
            if (arr[j] < arr[minIndex]) {
                minIndex = j;
            }
        }

        // Обмен местами
        std::swap(arr[i], arr[minIndex]);
    }
}
