// О(n)

void bubbleSort(int arr[], int n) {
    for (int i = 0; i < n - 1; i++) {          // Количество проходов
        for (int j = 0; j < n - i - 1; j++) {  // Сравнение соседей
            if (arr[j] > arr[j + 1]) {
                std::swap(arr[j], arr[j + 1]);
            }
        }
    }
}

